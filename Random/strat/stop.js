const DATE_OF_THE_WEEK = 1;

if ( DATE_OF_THE_WEEK < 8 && DATE_OF_THE_WEEK > 0){
	switch (DATE_OF_THE_WEEK){
		case 1:
			console.log("Monday");
			break;
	case 2:
		console.log("Tuesday");
		break;
	case 3:
		console.log("Wednesday");
		break;
	case 4:
		console.log("Thursday");
		break;
	case 5:
		console.log("Friday");
		break;
	case 6:
		console.log("Saturday");
		break;
	default:
		console.log("Sunday");
}}

